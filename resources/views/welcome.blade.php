        <h1 class="text-4xl font-bold text-blue-700 mb-4">Le Kabyle : une langue berbère</h1>

        <p>
            Le <strong>kabyle</strong> est une langue berbère parlée principalement en <strong>Kabylie</strong>, 
            une région située dans le nord de l’Algérie. 
            Elle compte environ <strong>5,5 millions de locuteurs dans le monde</strong>, dont <strong>3,5 millions en Kabylie</strong>, 
            et le reste réparti entre Alger, la France, le Canada et d’autres régions.
        </p>

        <p>
            Le kabyle est la <strong>langue berbère la plus parlée en Algérie</strong> et se classe 
            <strong>deuxième dans toute l’Afrique du Nord</strong>, juste après le <em>tachlhit</em> ou <em>chleuh</em>, 
            une autre variété berbère parlée au Maroc.
        </p>

        <p>
            Comme toutes les langues berbères, le kabyle fait partie de la <strong>famille des langues chamito-sémitiques</strong>. 
            Traditionnellement, il utilise l’écriture <strong>tifinagh</strong>, mais vers <strong>1945-1950</strong>, le kabyle a progressivement adopté 
            <strong>l’alphabet latin</strong>, notamment dans le cadre scolaire et institutionnel en Kabylie.
        </p>

        <p>
            En Algérie, le kabyle a longtemps été considéré comme un <strong>dialecte de la langue tamazight</strong>. 
            Après de nombreuses années de lutte pour sa reconnaissance, il est devenu <strong>deuxième langue nationale en 2002</strong>, 
            puis <strong>langue officielle en 2016</strong>. Aujourd’hui, il est enseigné à l’école, aux côtés du français.
        </p>

        <p>
            Le kabyle est fortement influencé par <strong>l’arabe et le français</strong>, et ses locuteurs pratiquent souvent 
            l’<em>alternance codique</em> (passage d’une langue à l’autre dans le même discours).
        </p>

